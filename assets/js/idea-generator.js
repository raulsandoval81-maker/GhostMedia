function renderGeneratedIdeas() {
  factoryList.innerHTML = "";

  generatedIdeas.forEach((title) => {
    const row = document.createElement("div");
    row.className = "page";
    row.innerHTML = `
      <strong>${title}</strong>
      <span>READY</span>
    `;
    factoryList.appendChild(row);
  });
}

generateBtn.addEventListener("click", () => {
  const page = factoryPage.value;
  generatedIdeas = IDEA_BANK[page] || [];
  renderGeneratedIdeas();
});

saveAllBtn.addEventListener("click", () => {
  if (!generatedIdeas.length) return;

  const ideas = gmGetIdeas();
  const page = factoryPage.value;

  generatedIdeas.forEach((title) => {
    ideas.unshift({
      id: Date.now() + Math.random(),
      title,
      page,
      notes: "Generated by Idea Factory.",
      status: "NEW",
      createdAt: new Date().toISOString()
    });
  });

  gmSaveIdeas(ideas);
  generatedIdeas = [];
  renderGeneratedIdeas();

  alert("Ideas saved.");
});